package com.mkpits.inheritance;

public class Super1 {
	
	int x=30;
	public Super1() {
	System.out.println("this is default constructor / no argument constructor of class x "+x);
	}
	public Super1(int x) {
		this.x = x;
		System.out.println("this is the Parameterized constructor of superclass x "+x);
	}
	

	}

